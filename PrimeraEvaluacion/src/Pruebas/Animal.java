package Pruebas;

public class Animal {

	private String tipo;
	private String sexo;
	
	public Animal(String tipo, String sexo) {
		this.tipo = tipo;
		this.sexo = sexo;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	
	
	
}
